#include "libft.h"

int	ft_intlen(long int num)
{
	unsigned int	i;

	i = 0;
	if (num < 0)
	{
		num = num * -1;
		i++;
	}
	while (num >= 10)
	{
		num = num / 10;
		i++;
	}
	i++;
	return (i);
}
